    public function delete($value,EntityManagerInterface $em){
        $ville = $this->find($value);
        foreach ($ville->getLieu() as $lieu){
            $em->getRepository(Lieu::class)->delete($lieu->getId(),$em);
        }
        $em->remove($ville);
        $em->flush();
    }

